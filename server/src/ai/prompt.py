def parse_ai_json(response: str | None) -> dict | None:
    """从AI响应中解析JSON"""
    if not response:
        return None
    try:
        return json.loads(response)
    except json.JSONDecodeError:
        import re
        match = re.search(r"\{[\s\S]*\}", response)
        if match:
            try:
                return json.loads(match.group())
            except json.JSONDecodeError:
                pass
    return None
